def main() -> None:
    PHASE2_DIR.mkdir(parents=True, exist_ok=True)

    FRONTEND_TS_PATH.write_text(generate_typescript(), encoding="utf-8")

    inventory = build_contract_inventory()
    glossary = CONTRACT_GLOSSARY
    schema_bundle = build_shared_schema_bundle()
    consistency_audit = build_platform_consistency_audit()
    legacy_mapping = build_legacy_mapping_summary()

    json_dump(PHASE2_DIR / "contract_inventory.json", inventory)
    json_dump(PHASE2_DIR / "contract_glossary.json", glossary)
    json_dump(PHASE2_DIR / "shared_contract_schemas.json", schema_bundle)
    json_dump(PHASE2_DIR / "consistency_audit.json", consistency_audit)
    json_dump(PHASE2_DIR / "legacy_mapping_summary.json", legacy_mapping)

    aligned = [
        item["contract"]
        for item in consistency_audit["current_platform_alignment"]
        if item["overall_status"] == "aligned"
    ]
    mismatched = [
        item["contract"]
        for item in consistency_audit["current_platform_alignment"]
        if item["overall_status"] != "aligned"
    ]
    summary = {
        "contract_count": len(CONTRACT_MODELS),
        "shared_python_vs_typescript_clean": all(
            item["status"] == "match" for item in consistency_audit["shared_python_vs_typescript"]
        ),
        "current_platform_aligned_contracts": aligned,
        "current_platform_mismatched_or_missing_contracts": mismatched,
        "legacy_full_fit_contracts": [item["target_contract"] for item in legacy_mapping if item["fit"] == "full_fit"],
        "legacy_partial_fit_contracts": [
            item["target_contract"] for item in legacy_mapping if item["fit"] != "full_fit"
        ],
    }
    json_dump(PHASE2_DIR / "summary.json", summary)

    print("Phase 2 contract freeze artifacts written.")
    print(f"Contracts frozen: {len(CONTRACT_MODELS)}")
    print(f"Shared Python/TypeScript parity clean: {summary['shared_python_vs_typescript_clean']}")
    print(f"Current platform aligned contracts: {len(aligned)}")
    print(f"Current platform mismatched or missing contracts: {len(mismatched)}")


if __name__ == "__main__":
    main()
