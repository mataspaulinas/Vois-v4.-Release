def build_platform_consistency_audit() -> dict[str, Any]:
    current_backend: dict[str, dict[str, Any]] = {}
    for schema_file in SCHEMA_FILES:
        current_backend.update(parse_python_schema_fields(schema_file))

    current_frontend = parse_ts_types(CURRENT_FRONTEND_API)
    shared_frontend = parse_ts_types(FRONTEND_TS_PATH)

    shared_python_index = {model.__name__: list(model.model_fields.keys()) for model in CONTRACT_MODELS}
    shared_py_vs_ts: list[dict[str, Any]] = []
    for contract_name, fields in shared_python_index.items():
        ts_fields = shared_frontend.get(contract_name, {}).get("fields", [])
        shared_py_vs_ts.append(
            {
                "contract": contract_name,
                "status": "match" if fields == ts_fields else "mismatch",
                "python_only": sorted(set(fields) - set(ts_fields)),
                "typescript_only": sorted(set(ts_fields) - set(fields)),
            }
        )

    platform_results: list[dict[str, Any]] = []
    for mapping in current_platform_mappings():
        contract_name = mapping["contract"]
        shared_fields = shared_python_index[contract_name]
        backend_name = mapping["backend"]
        frontend_name = mapping["frontend"]

        backend_fields = current_backend.get(backend_name, {}).get("fields", []) if backend_name else []
        frontend_fields = current_frontend.get(frontend_name, {}).get("fields", []) if frontend_name else []

        backend_status = "missing" if backend_name and backend_name not in current_backend else ("not_mapped" if not backend_name else "present")
        frontend_status = "missing" if frontend_name and frontend_name not in current_frontend else ("not_mapped" if not frontend_name else "present")

        comparisons = {
            "backend": compare_field_sets(shared_fields, backend_fields) if backend_status == "present" else None,
            "frontend": compare_field_sets(shared_fields, frontend_fields) if frontend_status == "present" else None,
        }

        if backend_status == "present" and frontend_status == "present":
            aligned = not comparisons["backend"]["missing_from_current"] and not comparisons["frontend"]["missing_from_current"]
            overall_status = "aligned" if aligned else "mismatch"
        else:
            overall_status = "missing_or_partial"

        platform_results.append(
            {
                "contract": contract_name,
                "overall_status": overall_status,
                "backend": {
                    "mapped_to": backend_name,
                    "status": backend_status,
                    "field_comparison": comparisons["backend"],
                },
                "frontend": {
                    "mapped_to": frontend_name,
                    "status": frontend_status,
                    "field_comparison": comparisons["frontend"],
                },
            }
        )

    return {
        "shared_python_vs_typescript": shared_py_vs_ts,
        "current_platform_alignment": platform_results,
    }


