def json_dump(path: Path, payload: Any) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(payload, indent=2, ensure_ascii=True) + "\n", encoding="utf-8")


def is_union(annotation: Any) -> bool:
    origin = get_origin(annotation)
    return origin in (types.UnionType, getattr(__import__("typing"), "Union", object))


def unwrap_optional(annotation: Any) -> tuple[Any, bool]:
    if not is_union(annotation):
        return annotation, False
    args = [arg for arg in get_args(annotation) if arg is not type(None)]
    if len(args) == 1 and len(args) != len(get_args(annotation)):
        return args[0], True
    return annotation, False


def render_ts_type(annotation: Any) -> str:
    annotation, optional = unwrap_optional(annotation)
    origin = get_origin(annotation)

    if annotation is Any:
        ts_type = "unknown"
    elif annotation in (str, date, datetime):
        ts_type = "string"
    elif annotation in (int, float):
        ts_type = "number"
    elif annotation is bool:
        ts_type = "boolean"
    elif origin in (list, list[str].__origin__):  # type: ignore[attr-defined]
        item_type = render_ts_type(get_args(annotation)[0])
        ts_type = f"{item_type}[]"
    elif origin in (dict, dict[str, str].__origin__):  # type: ignore[attr-defined]
        args = get_args(annotation)
        value_type = render_ts_type(args[1]) if len(args) == 2 else "unknown"
        ts_type = f"Record<string, {value_type}>"
    elif is_union(annotation):
        ts_type = " | ".join(sorted({render_ts_type(arg) for arg in get_args(annotation) if arg is not type(None)}))
    elif isinstance(annotation, type) and issubclass(annotation, StrEnum):
        ts_type = annotation.__name__
    elif isinstance(annotation, type) and any(annotation is model for model in CONTRACT_MODELS):
        ts_type = annotation.__name__
    else:
        ts_type = "unknown"

    if optional and "null" not in ts_type.split(" | "):
        ts_type = f"{ts_type} | null"
    return ts_type


def generate_typescript() -> str:
    lines: list[str] = [
        "// Generated by scripts/phase2_contract_freeze.py",
        "",
    ]

    for enum_type in ENUM_TYPES:
        members = " | ".join(f'"{member.value}"' for member in enum_type)
        lines.append(f"export type {enum_type.__name__} = {members};")
        lines.append("")

    for model in CONTRACT_MODELS:
        lines.append(f"export type {model.__name__} = {{")
        for field_name, field_info in model.model_fields.items():
            property_suffix = "?" if not field_info.is_required() else ""
            lines.append(f"  {field_name}{property_suffix}: {render_ts_type(field_info.annotation)};")
        lines.append("};")
        lines.append("")

    return "\n".join(lines).rstrip() + "\n"


def parse_python_schema_fields(path: Path) -> dict[str, dict[str, Any]]:
    module = ast.parse(path.read_text(encoding="utf-8"))
    classes: dict[str, dict[str, Any]] = {}
    for node in module.body:
        if not isinstance(node, ast.ClassDef):
            continue
        own_fields: list[str] = []
        for item in node.body:
            if isinstance(item, ast.AnnAssign) and isinstance(item.target, ast.Name):
                own_fields.append(item.target.id)
        bases = [base.id for base in node.bases if isinstance(base, ast.Name)]
        classes[node.name] = {"own_fields": own_fields, "bases": bases, "path": str(path)}

    def resolve(name: str, seen: set[str] | None = None) -> list[str]:
        seen = seen or set()
        if name in seen or name not in classes:
            return []
        seen.add(name)
        fields: list[str] = []
        for base in classes[name]["bases"]:
            fields.extend(resolve(base, seen))
        fields.extend(classes[name]["own_fields"])
        return list(dict.fromkeys(fields))

    resolved: dict[str, dict[str, Any]] = {}
    for name, info in classes.items():
        resolved[name] = {
            "fields": resolve(name),
            "path": info["path"],
        }
    return resolved


def parse_ts_types(path: Path) -> dict[str, dict[str, Any]]:
    lines = path.read_text(encoding="utf-8").splitlines()
    types_found: dict[str, dict[str, Any]] = {}
    i = 0
    start_re = re.compile(r"^export type (\w+) = (?:(\w+) & )?\{$")

    while i < len(lines):
        match = start_re.match(lines[i].strip())
        if not match:
            i += 1
            continue

        name, base = match.groups()
        depth = lines[i].count("{") - lines[i].count("}")
        i += 1
        own_fields: list[str] = []
        while i < len(lines) and depth > 0:
            line = lines[i]
            if depth == 1:
                field_match = re.match(r"\s*([A-Za-z_][A-Za-z0-9_]*)\??:", line)
                if field_match:
                    own_fields.append(field_match.group(1))
            depth += line.count("{") - line.count("}")
            i += 1
        types_found[name] = {"own_fields": own_fields, "base": base}

    def resolve(name: str, seen: set[str] | None = None) -> list[str]:
        seen = seen or set()
        if name in seen or name not in types_found:
            return []
        seen.add(name)
        fields: list[str] = []
        base = types_found[name]["base"]
        if base:
            fields.extend(resolve(base, seen))
        fields.extend(types_found[name]["own_fields"])
        return list(dict.fromkeys(fields))

    resolved: dict[str, dict[str, Any]] = {}
    for name, info in types_found.items():
        resolved[name] = {"fields": resolve(name), "base": info["base"]}
    return resolved


def build_contract_inventory() -> list[dict[str, Any]]:
    inventory: list[dict[str, Any]] = []
    for model in CONTRACT_MODELS:
        inventory.append(
            {
                "name": model.__name__,
                "field_count": len(model.model_fields),
                "fields": list(model.model_fields.keys()),
                "glossary": CONTRACT_GLOSSARY[model.__name__],
            }
        )
    return inventory


def build_shared_schema_bundle() -> dict[str, Any]:
    bundle = OrderedDict()
    for model in CONTRACT_MODELS:
        bundle[model.__name__] = {
            "fields": list(model.model_fields.keys()),
            "json_schema": model.model_json_schema(),
        }
    return bundle


def compare_field_sets(shared_fields: list[str], current_fields: list[str]) -> dict[str, list[str]]:
    shared_set = set(shared_fields)
    current_set = set(current_fields)
    return {
        "missing_from_current": sorted(shared_set - current_set),
        "extra_in_current": sorted(current_set - shared_set),
    }


